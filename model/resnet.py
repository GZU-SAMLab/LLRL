def resnet18_1(pretrained=False, custom_pretrained_weights=None, **kwargs):
    """Constructs a ResNet-18 model.

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
        custom_pretrained_weights (str): Path to custom pre-trained weights
    """
    model = ResNet(BasicBlock, [2, 2, 2, 2], **kwargs)

    # 加载自定义预训练权重
    if custom_pretrained_weights:
        assert os.path.exists(custom_pretrained_weights), \
            f"Pretrained weights file {custom_pretrained_weights} does not exist."

        # 加载预训练权重并过滤掉全连接层权重
        state_dict = torch.load(custom_pretrained_weights)
        state_dict = {k: v for k, v in state_dict.items() if not k.startswith('fc.')}

        model.load_state_dict(state_dict, strict=False)
        print("Loaded custom pre-trained weights for ResNet18 (excluding fully connected layers).")

    return model

